    private void savebuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebuttonActionPerformed
      String name = this.namebox.getText();
      int role;
       String password = this.passwordbox.getText();
       if(this.rolebox.getSelectedItem().equals("Admin")==true)
       role=100;
       else 
       role=200;
     if(name.equals("")==true || password.equals("")==true)
        {
            JOptionPane.showMessageDialog(null,"The form is incomplete");
        }
        else {        

          String query="insert into user(username,password,rid) values(?,?,?)";
        try {
          PreparedStatement  pst = db.con.prepareCall(query);
          pst.setString(1, name);
          pst.setString(2, password);
          pst.setInt(3, role);
          pst.execute();
           JOptionPane.showMessageDialog(null,"Recored Inserted Successfully");
            clearAll();
            pst.close();
            this.loadTable();
        }
        catch(Exception ex) {
            JOptionPane.showMessageDialog(null,"Failed to Insert the record Duplicate Username"+ex);
        }
    }
    }//GEN-LAST:event_savebuttonActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
     String uid = this.jTable1.getValueAt(this.jTable1.getSelectedRow(),0).toString();
     String query = "select * from user where uid="+uid;
     try {
         st = db.con.createStatement();
         result=st.executeQuery(query);
         result.next();
         this.idbox.setText(result.getString("uid"));
         this.namebox.setText(result.getString("username"));
         this.passwordbox.setText(result.getString("password"));
         this.updatebutton.setEnabled(true);
        this.deletebutton.setEnabled(true);
         st.close();
         result.close();
     }
     catch(Exception ex) {
         JOptionPane.showMessageDialog(null,ex);
     }
    }//GEN-LAST:event_jTable1MouseClicked

