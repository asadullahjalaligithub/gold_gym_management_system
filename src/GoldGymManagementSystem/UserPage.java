public void loadTable(){
        String query="SELECT user.*,role.* from user inner join role on user.rid=role.rid"; 
         int count,rowIndex=0;
            try {
             st = db.con.createStatement();
             result=st.executeQuery(query);
            result.next();
            result.last();
            count=result.getRow();// obtaining the number of rows in the table
            result.beforeFirst();
             String Data[][] = new String[count][4];

             while(result.next())
             {
                 Data[rowIndex][0] = String.valueOf(result.getInt("uid"));
                 Data[rowIndex][1]=result.getString("username");
                 Data[rowIndex][2]=result.getString("password");
                 Data[rowIndex][3]=result.getString("rname");
                 rowIndex++;
             }
             String cols[] = {"ID","Username","Password","Role"};
             DefaultTableModel table = new DefaultTableModel(Data,cols);
             this.jTable1.setModel(table);}
            catch(Exception ex)
            {
                JOptionPane.showMessageDialog(null, ex);
            }

    }
